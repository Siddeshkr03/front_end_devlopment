let arr = ["Bengaluru", "Tumakuru", "Mysore", "Udupi"];

// push -> adds the element to the last
arr.push("Hassan");
console.log(arr);

// pop -> removes the element from the last
console.log(arr.pop());
console.log(arr);

// shift -> removes the first element of an array
// unshift -> adds the element to the front

arr.unshift("Hassan");
console.log(arr);

arr.shift()
console.log(arr);

// sort method
arr.sort()
console.log(arr);

//reverse method
arr.reverse()
console.log(arr);



